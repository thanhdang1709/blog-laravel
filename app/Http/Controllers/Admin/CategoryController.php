     */
    public function store(Request $request)
    {
        
        try {
            $data = ['name'=>$request->name,'slug'=>\Str::slug($request->name)];
            $create = $this->categoryRepo->create($data);
            if ($create) {
                return redirect()->back()->with('success','Thêm category thành công!');
            }
        } catch (Exception $e) {
            return $e;
        }
    }

